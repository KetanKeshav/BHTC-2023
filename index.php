            <h2>Guidelines to Authors</h2>
            <p align="justify">
              Below are some useful instructions to assist the authors to submit their papers for BHTC 2023 -
              <br>
              <br>
              Papers submitted to BHTC 2023 shall contain original work by the author(s) that have not been published or submitted for publication elsewhere. The IEEE anti- plagiarism policy is applicable to all submissions. The author(s) are solely responsible for any plagiarized submission.

              <br>
              <br>
              <strong>Paper Submission:</strong> All papers are required to be prepared and submitted in the IEEE Standard two –column conference format of A4 size in English. Papers must be four to six pages in length, including texts, Figures, photographs and references. Any submitted paper that exceeds six pages will be rejected. The first page must include the title of the paper, author(s), affiliations, address and text. Please do not include page numbers on submitted documents. Failing to conform to the standard format will result in rejection. Detailed format information is available in IEEE Website.
              <br>
              The authors are required to use the standard IEEE manuscript template for conferences. The Microsoft Word template in can be downloaded from IEEE conference template page: Click here
              <br>
              <br>
              <strong>NOTE:</strong> Once the paper is submitted, we shall start reviewing it, so authors cannot update/resubmit the paper. They should submit the final version, update/resubmissions are not allowed.
              <br>
              <br>
              The paper shall consist of original and previously unpublished work, including specific results. The ‘Introduction’ of the paper shall clearly indicate the unique aspects of the submission and how it is related to the previous work.
              <br>
              <br>
              All paper submissions must be electronic, using IEEE Xplore compliant PDF format (*.pdf). Failure to do so may result in the rejection of the paper. The detailed template for paper submission can be found here - <a href="https://www.ieee.org/conferences/publishing/templates.html" target="_blank">https://www.ieee.org/conferences/publishing/templates.html</a>
              <br>
              <br>
              Type 3 fonts (bitmaps) will not be accepted. Authors can use the IEEE PDF eXpress to generate compliant PDF Files for final submission and details for the same would be shared after the review process with all the authors.
              <br><br>
              <strong>Paper Upload:</strong>
              All papers must be submitted in PDF or as a Microsoft Word Document via Microsoft Conference Management Toolkit : <a href="https://cmt3.research.microsoft.com/IEEEBHTC2023/" target="_blank"> https://cmt3.research.microsoft.com/IEEEBHTC2023/</a>
              <br><br>
              The submitting authors may be required to create a Microsoft Conference Management Toolkit account before uploading the paper (if the submitting author does not have a Microsoft Conference Management Toolkit account). While uploading the paper, the authors should provide the title of the paper, an abstract of no more than 250 words and keywords in the respective text boxes in the paper submission page.
              <br><br>
              Please note that all accepted papers that are registered and presented in the conference will be sent for possible inclusion in IEEE Xplore. The authors must agree to the IEEE copyright conditions and sign the IEEE copyright form as part of the online submission process. All submitted papers will be peer reviewed by the Technical Program Committees (TPC).
